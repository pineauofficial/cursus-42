#include "ft_printf.h"

int	ft_putstr(char *str)
{
	int	a;

	a = 0;
	if (!str)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (*str)
	{
		write(1, str++, 1);
		a++;
	}
	return (a);
}
